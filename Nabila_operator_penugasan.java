package nabila_operator_penugasan;

/**
 *
 * @author nabila sofiani
 */
public class Nabila_operator_penugasan {
    public static void main(String[] args) {
        int a;
        int b;
        
        //pengisian nilai
        a = 14;
        b = 8;
        
        //penambahan
        a += b;
        System.out.println("hasil penambahan : " +a);
        
        //pengurangan
        a -= b;
        System.out.println("hasil pengurangan :" +a);
        
        //perkalian
        a *= b;
        System.out.println("hasil perkalian : " +a);
        
        //pembagian
        a /= b;
        System.out.println("hasil pembagian : " +a);
        
        //sisa bagi
        a %= b;
        System.out.println("hasil sisa bagi : " +a);
        
    }
    
}
    

